-- Триггер для автоматического обновления updated_at в таблице card
CREATE OR REPLACE FUNCTION update_updated_at_column()
    RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ language 'plpgsql';

CREATE TRIGGER update_cart_updated_at
    BEFORE UPDATE ON cart
    FOR EACH ROW
EXECUTE FUNCTION update_updated_at_column();

-- Триггер для обновления updated_at в таблице orders
CREATE TRIGGER update_orders_updated_at
    BEFORE UPDATE ON orders
    FOR EACH ROW
EXECUTE FUNCTION update_updated_at_column();

-- Функция для расчета общей суммы корзины
CREATE OR REPLACE FUNCTION calculate_cart_total(cart_id_param INTEGER)
    RETURNS INTEGER AS $$
DECLARE
    total INTEGER;
BEGIN
    SELECT COALESCE(SUM(p.amount * ci.count_product), 0)
    INTO total
    FROM cart_item ci
             JOIN product p ON ci.product_id = p.id
    WHERE ci.cart_id = cart_id_param;

    RETURN total;
END;
$$ LANGUAGE plpgsql;

-- Функция для проверки доступности продукта
CREATE OR REPLACE FUNCTION check_product_availability()
    RETURNS TRIGGER AS $$
BEGIN
    IF (SELECT count FROM product WHERE id = NEW.product_id) < NEW.count_product THEN
        RAISE EXCEPTION 'Недостаточно товара на складе';
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER check_cart_item_availability
    BEFORE INSERT OR UPDATE ON cart_item
    FOR EACH ROW
EXECUTE FUNCTION check_product_availability();